package com.victor.base.data.entity;

public class StatisticsInfo {
    private String deviceNum;
    private String taskComplete;
    private String locationTotalNum;
    private String outNum;
    private String inNum;
    private String onlineNum;
    private String willAudit;
    private String audited;

    public String getDeviceNum() {
        return deviceNum;
    }

    public void setDeviceNum(String deviceNum) {
        this.deviceNum = deviceNum;
    }

    public String getTaskComplete() {
        return taskComplete;
    }

    public void setTaskComplete(String taskComplete) {
        this.taskComplete = taskComplete;
    }

    public String getLocationTotalNum() {
        return locationTotalNum;
    }

    public void setLocationTotalNum(String locationTotalNum) {
        this.locationTotalNum = locationTotalNum;
    }

    public String getOutNum() {
        return outNum;
    }

    public void setOutNum(String outNum) {
        this.outNum = outNum;
    }

    public String getInNum() {
        return inNum;
    }

    public void setInNum(String inNum) {
        this.inNum = inNum;
    }

    public String getOnlineNum() {
        return onlineNum;
    }

    public void setOnlineNum(String onlineNum) {
        this.onlineNum = onlineNum;
    }

    public String getWillAudit() {
        return willAudit;
    }

    public void setWillAudit(String willAudit) {
        this.willAudit = willAudit;
    }

    public String getAudited() {
        return audited;
    }

    public void setAudited(String audited) {
        this.audited = audited;
    }
}
